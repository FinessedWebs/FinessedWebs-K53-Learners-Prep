let questions = [];
let currentIndex = 0;
let score = {
    roadRules: 0,
    roadSigns: 0,
    vehicleControls: 0
};

let timerInterval;
let timeLeft = 60 * 60;

// START EXAM
function startExam(timed) {
    localStorage.setItem("timedMode", timed);

    document.getElementById("start-screen").style.display = "none";
    document.getElementById("exam-screen").style.display = "block";

    buildExam();

    if (timed) startTimer();

    showQuestion();
}

// BUILD EXAM (shuffle)
function buildExam() {
    questions = [
        ...getRandomQuestions(questionBank.roadRules, 30),
        ...getRandomQuestions(questionBank.roadSigns, 30),
        ...getRandomQuestions(questionBank.vehicleControls, 8)
    ];

    questions = shuffle(questions);
}

function getRandomQuestions(array, count) {
    const shuffled = shuffle([...array]);

    if (shuffled.length < count) {
        console.warn(`Not enough questions in ${array[0]?.category}`);
    }

    return shuffled.slice(0, count);
}

// SHUFFLE FUNCTION
function shuffle(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// SHOW QUESTION
function showQuestion() {
    const q = questions[currentIndex];

if (q.image) {
    const img = document.createElement("img");
    img.src = "images/" + q.image;
    img.style.maxWidth = "200px";
    document.getElementById("question").appendChild(img);
}

    document.getElementById("category").innerText = q.category;
    document.getElementById("progress").innerText =
        `Question ${currentIndex + 1} of ${questions.length}`;

    document.getElementById("question").innerText = q.question;

    const optionsDiv = document.getElementById("options");
    optionsDiv.innerHTML = "";

    // 🔥 Shuffle options safely
    const shuffledOptions = shuffle([...q.options]);

    shuffledOptions.forEach(opt => {
        const div = document.createElement("div");
        div.className = "option";
        div.innerText = opt;

        div.onclick = () => selectAnswer(div, opt, q);

        optionsDiv.appendChild(div);
    });

    document.getElementById("nextBtn").style.display = "none";
}

// SELECT ANSWER
function selectAnswer(element, selectedOption, question) {
    const options = document.querySelectorAll(".option");

    options.forEach(opt => opt.onclick = null);

    if (selectedOption === question.answer) {
        element.classList.add("correct");
        score[question.category]++;
    } else {
        element.classList.add("wrong");

        options.forEach(opt => {
            if (opt.innerText === question.answer) {
                opt.classList.add("correct");
            }
        });
    }

    document.getElementById("nextBtn").style.display = "block";
}

// NEXT
function nextQuestion() {
    currentIndex++;

    if (currentIndex >= questions.length) {
        finishExam();
        return;
    }

    showQuestion();
}

// TIMER
function startTimer() {
    timerInterval = setInterval(() => {
        timeLeft--;

        let minutes = Math.floor(timeLeft / 60);
        let seconds = timeLeft % 60;

        document.getElementById("timer").innerText =
            `${minutes}:${seconds.toString().padStart(2, "0")}`;

        if (timeLeft <= 0) {
            clearInterval(timerInterval);
            finishExam();
        }
    }, 1000);
}

// FINISH
function finishExam() {
    localStorage.setItem("results", JSON.stringify(score));
    window.location.href = "results.html";
}